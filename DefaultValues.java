//1.2 Write a Java program to display the default value of all primitive data types of Java

package practiceProblem;

public class DefaultValues {
      static int a;
      static double b;
      static char c;
      static boolean e;
      static byte h;



      public static void main(String[] args) {
        System.out.println("default value of integer is: " + a );
        System.out.println("default value of double is: " + b );
        System.out.println("default value of char is: " + c );
        System.out.println("default value of boolean is: " + e );
        System.out.println("default value of byte is: " + h );
      }
}
